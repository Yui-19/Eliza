@catub.cat_cmd(
    pattern="ping( -a|$)",
    command=("ping", plugin_category),
    info={
        "header": "Check how long it takes to ping your userbot",
        "flags": {"-a": "average ping"},
        "usage": ["{tr}ping", "{tr}ping -a"],
    },
)
async def _(event):

    "To check ping"

    flag = event.pattern_match.group(1)

    start = datetime.now()

    if flag == " -a":

        catevent = await edit_or_reply(event, "`!....`")

        await asyncio.sleep(0.3)

        await catevent.edit("`..!..`")

        await asyncio.sleep(0.3)

        await catevent.edit("`....!`")

        end = datetime.now()

        tms = (end - start).microseconds / 1000

        ms = round((tms - 0.6) / 3, 3)

        await catevent.edit(
            f"{AVG_TEXT}\n{ms} {PING_PARTNER}\n{PING_MENTION} {hmention}",
            parse_mode="html",
        )

    else:

        catevent = await edit_or_reply(event, f"{PING_TEXT}", "html")

        end = datetime.now()

        ms = (end - start).microseconds/1000

        await catevent.edit(
            f"{PONG_TEXT}\n{ms} {PING_PARTNER}\n{PING_MENTION} {hmention}",
            parse_mode="html",
        )


